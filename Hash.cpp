	static void g_N(const byte *N, byte *h, const byte *m)
	{
		byte t[64], K[64];

		AddXor512(N, h, K);
		F(K);
		E(K, m, t);
		AddXor512(t, h, t);
		AddXor512(t, m, h);
	}

	void hash(byte* hash, const byte* message, const uint length)
	{
		byte v512[64]  = {0};
		byte v0[64]    = {0};
		byte Sigma[64] = {0};
		byte N[64]     = {0};
		byte m[64];
		u64 len = length*8;

		memset(hash, 0, 64);
		v512[62] = 0x02;

		// Stage 2
		while (len >= 512)
		{
			memcpy(m, message + len/8 - 63 - ( (len & 0x7) == 0 ), 64);

			g_N(N, hash, m);
			AddModulo512(N, v512, N);
			AddModulo512(Sigma, m, Sigma);
			len -= 512;
		}

		memset(m, 0, 64);
		memcpy(m + 63 - len/8 + ( (len & 0x7) == 0 ), message, (uint)(len/8 + 1 - ( (len & 0x7) == 0 )));

		// Stage 3
		m[63 - len/8] |= (1 << (len & 0x7));

		g_N(N, hash, m);
		v512[63] = len & 0xFF;
		v512[62] = (byte)(len >> 8);
		AddModulo512(N, v512, N);

		AddModulo512(Sigma, m, Sigma);

		g_N(v0, hash, N);
		g_N(v0, hash, Sigma);
	}
